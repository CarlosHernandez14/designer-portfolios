    private void btnAddImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddImageActionPerformed
        // TODO add your handling code here:
        
        JnaFileChooser fc = new JnaFileChooser();

        fc.setTitle("Seleccionar imagenes");
        fc.setMultiSelectionEnabled(true);
        fc.addFilter("Todos los archivos", "*");
        fc.addFilter("Imagenes", "*.jpg", "*.png", "*.jpeg");        
        
        if (fc.showOpenDialog(this)) {
            File[] files = fc.getSelectedFiles();
            selectedImages.addAll(Arrays.asList(files));
            loadImages();
        }

    }//GEN-LAST:event_btnAddImageActionPerformed

    private void btnCreateProjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateProjectActionPerformed
        // TODO add your handling code here:
        
        String tittle = this.fieldTittle.getText();
        String description = this.fieldDescription.getText();
        
        java.sql.Date startDate = new java.sql.Date(this.dateChooserStart.getDate().getTime());
        java.sql.Date endDate = new java.sql.Date(this.dateChooserFin.getDate().getTime());
        
        Project newProject = new Project(this.loggedProfile.getIdProfile(), tittle, description, startDate, endDate, endDate == null || endDate.toString().isEmpty());
        int newProjectId = ProjectsDao.insertProject(newProject);
        if (newProjectId != -1) {
            // Save images
            try {
                
                List<Image> images = new ArrayList<>();
                for (File selectedImage : this.selectedImages) {
                    images.add(new Image(Files.readAllBytes(selectedImage.toPath()), newProjectId));
                }
                if (ProjectsDao.saveImages(images)) {
                    JOptionPane.showMessageDialog(null, "Proyecto creado con exito");
                    this.homeWindow.initMyPortfolios();
                    this.dispose();
                } else 
                    JOptionPane.showMessageDialog(null, "Ocurrio un error al guardar las imagenes");
                
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Ocurrio un error al agregar las imagenes");
                System.out.println("Error al guardar imagenes : " + e.getMessage());
            }
            
        } else
            JOptionPane.showMessageDialog(null, "Ocurrio un error al crear el proyecto");
    }//GEN-LAST:event_btnCreateProjectActionPerformed

