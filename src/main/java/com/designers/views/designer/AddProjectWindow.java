    public void loadImages() {
        
        this.containerImages.removeAll();
        
        for (File selectedImage : selectedImages) {
            PanelImagePreview pImgPrev = new PanelImagePreview(selectedImage, this);
            this.containerImages.add(pImgPrev);
        }
        
        containerImages.revalidate();
        containerImages.repaint();
    }
    
    public void deleteImage(File image) {
        
        this.selectedImages.remove(image);
        loadImages();
        
    }

